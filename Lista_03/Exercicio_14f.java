package ListaTresAtribuicaoOperadores;

public class Exercicio_14f {

	public static void main(String[] args) {
		int X, Y, Z;

		X = 1;
		Y = 2;

		Z = Y - X;

		System.out.println("Z: " + Z);

		X = 5;

		Y = X + Z;

		System.out.println("X: " + X + " Y: " + Y + " Z: " + Z);
	}

}

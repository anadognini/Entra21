package ListaTresAtribuicaoOperadores;

public class Exercicio_14b {

	public static void main(String[] args) {
		int A, B, C;
		
		A = 30;
		B = 20;
		
		C = A + B;
		
		System.out.println("C: " + C);
		
		B = 10;
		
		System.out.println("B: " + B + " C: " + C);
		
		C = A + B;
		
		System.out.println("A: " + A + " B: " + B + " C: " + C);
	}

}
